namespace Interfaces.Factory.CommonTypes
{
  public class BindingVector3
  {
    private float x;
    private float y;
    private float z;

    public float X
    {
      get { return x; }
      set { x = value; }
    }
    public float Y
    {
      get { return y; }
      set { y = value; }
    }
    public float Z
    {
      get { return z; }
      set { z = value; }
    }
  }
}